import Foundation
import UIKit

extension UIColor {
    
    class func lightBlue() -> UIColor {
        return UIColor(red: 173/255, green: 216/255, blue: 230/255, alpha: 1)
    }
    
    class func darkGray()-> UIColor  {
        return UIColor(red: 169/255, green: 169/255, blue: 169/255, alpha: 1)
    }
}
